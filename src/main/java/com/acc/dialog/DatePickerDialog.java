    @Override
    public void keyReleased(KeyEvent e) {
        Object sourceObj = e.getSource();
        String ctrlName = "-";

        if (sourceObj instanceof JTextFieldDateEditor) {
            ctrlName = ((JTextFieldDateEditor) sourceObj).getName();
        } else if (sourceObj instanceof JButton) {
            ctrlName = ((JButton) sourceObj).getName();
        }
        switch (ctrlName) {
            case "txtFromDate":
                switch (e.getKeyCode()) {
                    case KeyEvent.VK_ENTER:
                        if (sourceObj != null) {
                            String date = ((JTextFieldDateEditor) sourceObj).getText();
                            if (date.length() == 8) {
                                String toFormatDate = Util1.toFormatDate(date);
                                txtFromDate.setDate(Util1.toDate(toFormatDate, "dd/MM/yyyy"));
                            }
                        }
                        txtToDate.requestFocusInWindow();
                        break;
                    case KeyEvent.VK_DOWN:
                        txtToDate.requestFocus();
                        break;
                    case KeyEvent.VK_UP:
                        btnOK.requestFocus();
                        break;

                }

                break;
            case "txtToDate":
                switch (e.getKeyCode()) {
                    case KeyEvent.VK_ENTER:
                        if (sourceObj != null) {
                            String date = ((JTextFieldDateEditor) sourceObj).getText();
                            if (date.length() == 8) {
                                String toFormatDate = Util1.toFormatDate(date);
                                txtToDate.setDate(Util1.toDate(toFormatDate, "dd/MM/yyyy"));
                            }
                        }
                        btnOK.requestFocus();
                        break;
                    case KeyEvent.VK_UP:
                        btnOK.requestFocus();
                        break;
                }
                break;

            case "btnOK":
                switch (e.getKeyCode()) {
                    case KeyEvent.VK_ENTER:
                        sendDate();
                        break;
                    case KeyEvent.VK_UP:
                        txtFromDate.getDateEditor().getUiComponent().requestFocusInWindow();
                        break;
                }

                break;

        }

    }
