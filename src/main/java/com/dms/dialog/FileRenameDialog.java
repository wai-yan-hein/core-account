    private void selectTextBeforeLastDot() {
        String text = txtFileName.getText();
        int lastDotIndex = text.lastIndexOf('.');
        if (lastDotIndex != -1) {
            txtFileName.setSelectionStart(0);
            txtFileName.setSelectionEnd(lastDotIndex);
        }
    }

