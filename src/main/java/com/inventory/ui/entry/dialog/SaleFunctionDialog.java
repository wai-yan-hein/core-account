    @Override
    public void keyReleased(KeyEvent e) {
        Object sourceObj = e.getSource();
        String ctrlName = "-";
        if (sourceObj instanceof JFormattedTextField txt) {
            ctrlName = txt.getName();
        }
        switch (ctrlName) {
            case "txtDisPer" -> {
                if (e.getKeyCode() == KeyEvent.VK_ENTER) {
                    txtDisAmt.setValue(0);
                    calDiscount();
                }
            }
            case "txtDisAmt" -> {
                if (e.getKeyCode() == KeyEvent.VK_ENTER) {
                    txtDisPer.setValue(0);
                    calDiscount();
                }
            }
        }
    }
