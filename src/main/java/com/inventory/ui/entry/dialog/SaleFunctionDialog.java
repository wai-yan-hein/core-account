    public void setSaleDetail(SaleHisDetail sd) {
        this.sd = sd;
        lblStockName.setText(sd.getStockName());
        txtDisAmt.setValue(sd.getDisAmt());
        txtDisPer.setValue((sd.getDisAmt() / sd.getAmount()) * 100);
        txtItemAmt.setValue(sd.getAmount());
        txtDisPer.requestFocus();

    }

    private void initTextBox() {
        ComponentUtil.setTextProperty(this);
        ComponentUtil.addFocusListener(this);
        txtDisAmt.addKeyListener(this);
        txtDisPer.addKeyListener(this);
        txtChangeAmt.setForeground(Color.red);
    }

    private void calDiscount() {
        double disP = Util1.getDouble(txtDisPer.getValue());
        double disA = Util1.getDouble(txtDisAmt.getValue());
        double amount = sd.getAmount();
        if (disP > 0) {
            double result = (amount * (disP / 100));
            txtDisAmt.setValue(Util1.getDouble(result));
        } else if (disA > 0) {
            double result = (disA / amount) * 100;
            txtDisPer.setValue(Util1.getDouble(result));
        }
        disA = Util1.getDouble(txtDisAmt.getValue());
        if (disA > 0) {
            double chAmt = amount - disA;
            txtChangeAmt.setValue(chAmt);
        }
    }

    private void setFoc() {
        int yn = JOptionPane.showConfirmDialog(this, "Are you soure FOC", "Set FOC", JOptionPane.YES_OPTION);
        if (yn == JOptionPane.YES_OPTION) {
            sd.setStockName(sd.getStockName() + " * FOC");
            sd.setFoc(true);
            sd.setAmount(0);
            dispose();
        } else {
            sd.setFoc(false);
        }
    }

    private void confirm() {
        double chAmt = Util1.getDouble(txtChangeAmt.getValue());
        if (chAmt > 0) {
            sd.setDisAmt(Util1.getDouble(txtDisAmt.getValue()));
            sd.setAmount(chAmt);
            sd.setStockName(sd.getStockName() + " * DIS");
        }
        setConfirm(true);
        this.dispose();
    }

