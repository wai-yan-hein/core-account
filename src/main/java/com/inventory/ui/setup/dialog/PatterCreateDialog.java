    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
        if (status.equals("NEW")) {
            clear();
        } else {
            lblStatus.setText(status);
            txtUserCode.setText(pattern.getUserCode());
            txtName.setText(pattern.getPatternName());
            chkActive.setSelected(pattern.isActive());
            vouStatusAutoCompleter.setVoucher(pattern.getVouStatus());
        }
    }

    public Pattern getPattern() {
        return pattern;
    }

    public void setPattern(Pattern pattern) {
        this.pattern = pattern;
    }

