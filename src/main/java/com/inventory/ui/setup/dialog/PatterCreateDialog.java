    public void initCombo() {
        vouStatusAutoCompleter = new VouStatusAutoCompleter(txtVouType, Global.listVouStatus, null, false);
        vouStatusAutoCompleter.setVoucher(null);
    }

    private void save() {
        status = lblStatus.getText();
        if (isValidEntry()) {
            pattern.setUserCode(txtUserCode.getText());
            pattern.setPatternName(txtName.getText());
            pattern.setVouStatus(vouStatusAutoCompleter.getVouStatus());
            pattern.setActive(chkActive.isSelected());
            clear();
            this.dispose();
        }
    }

    private boolean isValidEntry() {
        boolean valid = true;
        if (Util1.isNull(txtName.getText())) {
            valid = false;
            txtName.requestFocus();
        } else if (vouStatusAutoCompleter.getVouStatus() == null) {
            valid = false;
            txtVouType.requestFocus();
        }
        return valid;
    }

    private void clear() {
        txtUserCode.setText(null);
        txtName.setText(null);
        lblStatus.setText("NEW");
    }

